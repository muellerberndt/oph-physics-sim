from __future__ import annotations

import json
import math
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import numpy as np

from oph_fpe.constants.oph_pixel import OPHPixelConstants, P_STAR


@dataclass(frozen=True)
class ScalarRepairSemigroupSpec:
    """Finite scalar-repair semigroup audit input.

    The compact OPH repair theorem identifies exact support-visible Euclidean
    repair with a semigroup generated by sums of conditional-expectation
    complements. This small scalar audit checks the one-mode clock normalization
    needed by the exact CMB branch, without promoting a declared target into a
    finite-lattice derivation.
    """

    dimension: int = 33
    kappa_rep: float = math.e
    source: str = "declared_euler_repair_time_target"
    finite_lattice_derived: bool = False
    matrix_source: str | None = None
    p_value: float = P_STAR


def scalar_repair_semigroup_report(spec: ScalarRepairSemigroupSpec | None = None) -> dict[str, Any]:
    spec = spec or ScalarRepairSemigroupSpec()
    if spec.dimension < 2:
        raise ValueError("dimension must be at least 2")
    pixel = OPHPixelConstants(P=float(spec.p_value))
    delta_p = float(pixel.P - pixel.phi)
    target_kappa = math.e
    target_gap = float(target_kappa * delta_p)
    declared_gap = float(spec.kappa_rep * delta_p)
    projector = _centered_projector(spec.dimension)
    generator = declared_gap * projector
    eigvals = np.linalg.eigvalsh(generator)
    nonzero = eigvals[eigvals > 1.0e-12]
    centered_gap = float(np.min(nonzero)) if nonzero.size else 0.0
    constants_zero = bool(abs(float(eigvals[0])) < 1.0e-10)
    centered_scalar = bool(nonzero.size == spec.dimension - 1 and float(np.std(nonzero)) < 1.0e-10)
    contractive = bool(np.all(np.exp(-eigvals) <= 1.0 + 1.0e-12))
    relative_error = abs(float(spec.kappa_rep) - target_kappa) / target_kappa
    controls = _wrong_kappa_controls(delta_p=delta_p, target_gap=target_gap)
    semigroup_controls_passed = bool(controls["target_gap_beats_wrong_kappas"])
    eligible = bool(
        spec.finite_lattice_derived
        and spec.source == "finite_state_transition_matrix"
        and constants_zero
        and centered_scalar
        and contractive
        and semigroup_controls_passed
        and relative_error <= 0.05
    )
    report = {
        "mode": "oph_scalar_repair_semigroup_gap_audit_v0",
        "source": spec.source,
        "matrix_source": spec.matrix_source,
        "dimension": int(spec.dimension),
        "centered_subspace_dimension": int(spec.dimension - 1),
        "target": {
            "formula": "eta_R = kappa_rep * (P - phi)",
            "required_kappa_rep": target_kappa,
            "required_gap": target_gap,
            "required_eta_R": target_gap,
            "required_n_s": 1.0 - target_gap,
            "P": float(pixel.P),
            "phi": float(pixel.phi),
            "delta_P": delta_p,
        },
        "semigroup": {
            "generator_form": "L_scalar = kappa_rep * (P - phi) * (I - E_constants)",
            "constant_mode_zero": constants_zero,
            "centered_scalar_relaxation": centered_scalar,
            "centered_gap": centered_gap,
            "kappa_rep_estimate": float(spec.kappa_rep),
            "eta_R_estimate": float(spec.kappa_rep * delta_p),
            "n_s_estimate": float(1.0 - spec.kappa_rep * delta_p),
            "contractive_at_t1": contractive,
            "eigenvalue_min": float(np.min(eigvals)),
            "eigenvalue_max": float(np.max(eigvals)),
            "relative_error_to_kappa_e": float(relative_error),
        },
        "controls": controls,
        "semigroup_controls_passed": semigroup_controls_passed,
        "finite_lattice_derived": bool(spec.finite_lattice_derived),
        "eligible_for_repair_clock_certificate": eligible,
        "repair_clock_certificate": eligible,
        "SEMIGROUP_TARGET_RECEIPT": bool(
            constants_zero and centered_scalar and contractive and relative_error <= 1.0e-12
        ),
        "claim_boundary": (
            "Scalar repair-semigroup gap audit for the exact OPH CMB branch. "
            "A declared Euler target can verify the algebraic normalization path, "
            "but only a source='finite_state_transition_matrix' report with "
            "finite_lattice_derived=true is eligible to close the finite repair-clock certificate."
        ),
    }
    return report


def write_scalar_repair_semigroup_report(
    out_dir: Path,
    *,
    dimension: int = 33,
    kappa_rep: float = math.e,
    source: str = "declared_euler_repair_time_target",
    finite_lattice_derived: bool = False,
    matrix_source: str | None = None,
    p_value: float = P_STAR,
) -> dict[str, Any]:
    report = scalar_repair_semigroup_report(
        ScalarRepairSemigroupSpec(
            dimension=dimension,
            kappa_rep=kappa_rep,
            source=source,
            finite_lattice_derived=finite_lattice_derived,
            matrix_source=matrix_source,
            p_value=p_value,
        )
    )
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "scalar_repair_semigroup_report.json").write_text(
        json.dumps(report, indent=2, default=str),
        encoding="utf-8",
    )
    (out_dir / "scalar_repair_semigroup_report.md").write_text(_markdown_report(report), encoding="utf-8")
    return report


def _centered_projector(dimension: int) -> np.ndarray:
    one = np.ones((dimension, 1), dtype=np.float64)
    return np.eye(dimension, dtype=np.float64) - (one @ one.T) / float(dimension)


def _wrong_kappa_controls(*, delta_p: float, target_gap: float) -> dict[str, Any]:
    candidates = {
        "wrong_1x": 1.0,
        "wrong_pi": math.pi,
        "wrong_2pi": 2.0 * math.pi,
        "correct_e": math.e,
    }
    rows = []
    for name, kappa in candidates.items():
        gap = float(kappa * delta_p)
        rows.append(
            {
                "name": name,
                "kappa_rep": float(kappa),
                "gap": gap,
                "absolute_gap_error": abs(gap - target_gap),
            }
        )
    best = min(rows, key=lambda row: row["absolute_gap_error"])
    return {
        "rows": rows,
        "best_control": best["name"],
        "target_gap_beats_wrong_kappas": best["name"] == "correct_e",
    }


def _markdown_report(report: dict[str, Any]) -> str:
    target = report["target"]
    semigroup = report["semigroup"]
    return "\n".join(
        [
            "# OPH Scalar Repair-Semigroup Gap Audit",
            "",
            report["claim_boundary"],
            "",
            "## Target",
            "",
            f"- required kappa_rep: `{target['required_kappa_rep']:.12g}`",
            f"- required eta_R: `{target['required_eta_R']:.12g}`",
            f"- required n_s: `{target['required_n_s']:.12g}`",
            "",
            "## Semigroup",
            "",
            f"- source: `{report['source']}`",
            f"- finite lattice derived: `{report['finite_lattice_derived']}`",
            f"- centered gap: `{semigroup['centered_gap']:.12g}`",
            f"- kappa_rep estimate: `{semigroup['kappa_rep_estimate']:.12g}`",
            f"- eligible for repair-clock certificate: `{report['eligible_for_repair_clock_certificate']}`",
            f"- repair-clock certificate: `{report['repair_clock_certificate']}`",
            "",
            "## Files",
            "",
            "- `scalar_repair_semigroup_report.json`",
            "- `scalar_repair_semigroup_report.md`",
            "",
        ]
    )
